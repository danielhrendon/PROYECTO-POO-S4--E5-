    private void buscaPKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_buscaPKeyPressed
    {//GEN-HEADEREND:event_buscaPKeyPressed

        Validaciones.enter(this, evt, new JTextField());
        if (evt.getKeyChar() == '\n')
        {

            if (flag3 == 1)
            {
                obj.terminarmodelo(this, buscaP.getText());
            } else
            {
                int bandera = 0;

                for (int i = 0; i < obj.getSucursales().size(); i++)
                {
                    for (int j = 0; j < obj.getAutoServicios().get(i).size(); j++)
                    {
                        if (((Modelo) obj.getAutoServicios().get(i).get(j)).getCalzado().equals(buscaP.getText()))
                        {

                            new muestraModelo().setVisible(true);
                            bandera = 0;
                        } else
                        {
                            bandera = 1;
                        }
                        if (bandera == 0)
                        {
                            break;
                        }
                    }
                }

                if (bandera == 1)
                {
                    Mensaje.error(this, "No se encontr贸 el modelo. Verifique que est茅 escrita correctamente.");
                } else
                {
                    muestraModelo.muestraA.setText(obj.buscarmodelo(buscaP.getText()));
                }
            }

        }
    }//GEN-LAST:event_buscaPKeyPressed

