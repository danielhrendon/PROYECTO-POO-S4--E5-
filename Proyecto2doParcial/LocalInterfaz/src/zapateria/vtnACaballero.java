    private void posSucuKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_posSucuKeyPressed
    {//GEN-HEADEREND:event_posSucuKeyPressed
        Validaciones.enter(this, evt, new JTextField());
        if (evt.getKeyChar() == '\n')
        {
            if ("".equals(posSucu.getText()))
            {
                Mensaje.error(this, "Debe teclear la posición de la sucursal. Para salir, presione cancelar.");
            } else
            {
                fecha.setEnabled(true);
                costo.setEnabled(true);
                SoN.setEnabled(true);
                posSucu.setEnabled(false);
                this.btnCambiar.setVisible(true);
                this.cambiar.setVisible(true);
                if (!"".equals(fecha.getText()) && !"".equals(costo.getText()) && !"".equals(SoN.getText()) && !"".equals(posSucu.getText()))
                {
                    btnSiguiente.setEnabled(true);
                    siguiente.setEnabled(true);
                }
            }
        }

    }//GEN-LAST:event_posSucuKeyPressed

