    private void btnSiguiente1MouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btnSiguiente1MouseClicked
    {//GEN-HEADEREND:event_btnSiguiente1MouseClicked
        if (!"".equals(fecha.getText()) && !"".equals(costo.getText()) && !"".equals(SoN.getText()) && !"".equals(posSucu.getText()))
        {
            modelo.setEnabled(true);
            anio.setEnabled(true);
            tipocalzado.setEnabled(true);
            jLabel12.setEnabled(true);
            jLabel8.setEnabled(true);
            jLabel9.setEnabled(true);
            jLabel10.setEnabled(true);
            btnTerminar.setVisible(true);
            terminar.setVisible(true);
            //QUITAR BOTON CAMBIAR
            this.btnCambiar.setVisible(false);
            this.cambiar.setVisible(false);
        } else
        {
            Mensaje.error(this, "Por favor, llene todos los campos.");
        }
    }//GEN-LAST:event_btnSiguiente1MouseClicked

    private void btnTerminar1MouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btnTerminar1MouseClicked
    {//GEN-HEADEREND:event_btnTerminar1MouseClicked
        if (!"".equals(fecha.getText()) && !"".equals(costo.getText()) && !"".equals(SoN.getText()) && !"".equals(posSucu.getText()) && !"".equals(modelo.getText()) && !"".equals(anio.getText()) && !"".equals(tipocalzado.getText()))
        {
//            if (obj2.validaSucursal(Integer.parseInt(posSucu.getText()), obj.getAutoServicios().size()) == true)
            if (Integer.parseInt(posSucu.getText()) < obj.getAutoServicios().size() && Integer.parseInt(posSucu.getText()) >= 0)
            {

                obj.agregarmodelo(Integer.parseInt(posSucu.getText()), new Caballero(fecha.getText(), costo.getText(), SoN.getText(), modelo.getText(), Integer.parseInt(anio.getText()), tipocalzado.getText()));

            } else
            {
                Mensaje.error(this, "Esa posición de sucursal no está registrada.");
            }
            this.dispose();
        } else
        {
            Mensaje.error(this, "Por favor, llene todos los campos.");
        }
    }//GEN-LAST:event_btnTerminar1MouseClicked

